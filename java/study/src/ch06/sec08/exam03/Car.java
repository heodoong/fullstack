package ch06.sec08.exam03;

public class Car {
    int gas;
    String model;
    boolean full;

    public String getModel() {
        return model;
    }

    public boolean isFull() {
        return full;
    }

    public void setFull(boolean full) {
        this.full = full;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getGas() {
        return gas;
    }

    public void setGas(int gas) {
        this.gas = gas;
    }
}


